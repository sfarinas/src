package api;

public class ExplorandoMath {

	public static void main(String[] args) {
		
		System.out.println(Math.PI);
		
		// EXPONENCIAÇÃO
		System.out.println(Math.pow(2, 3));
		
		// VALOR ABSOLUTO
		System.out.println(Math.abs(-10.5));
		
		// PEGAR O MENOR VALOR
		System.out.println(Math.min(70, 5));
		
		// PEGAR O MAIOR VALOR
		System.out.println(Math.max(12540, 9876));
		
		// ARREDONDAMENTO DO VALOR PARA CIMA
		System.out.println(Math.ceil(6.55));
		
		// ARREDONDAMENTO DO VALOR PARA BAIXO
		System.out.println(Math.floor(6.55));
		
		// VALOR ALEATORIO
		System.out.println(Math.random());
		
	}
}
